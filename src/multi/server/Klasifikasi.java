    private void SAVEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SAVEActionPerformed
        // TODO add your handling code here:
        
        try 
        {
            Connection konek = Koneksi.getKoneksi(); 
            Statement state = konek.createStatement();
            String query = "INSERT INTO Antrian VALUES(?,?,?,?,?,?,?,?,?,?,?)";
            PreparedStatement prepare = konek.prepareStatement(query);
        
            prepare.setString(1, Lamda.getText());
            prepare.setString(2, Miu.getText());
            prepare.setString(3, Nilai_s.getText());
            prepare.setString(4, DataI.getText());
            prepare.setString(5, Hasil_Po.getText());
            prepare.setString(6, hasil_pw.getText());
            prepare.setString(7, hasil_Pm.getText());
            prepare.setString(8, hasil_lq.getText());
            prepare.setString(9, hasil_ls.getText());
            prepare.setString(10, hasil_ws.getText());
            prepare.setString(11, hasil_wq.getText());
            prepare.executeUpdate();
        
            JOptionPane.showMessageDialog(null,"Data telah disimpan");
            prepare.close();
        }
      catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,"Data gagal disimpan ");
            System.out.println(ex);   
        }
     
  
      
    }//GEN-LAST:event_SAVEActionPerformed

