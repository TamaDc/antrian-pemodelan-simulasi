    private void OPERASIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OPERASIActionPerformed
        // TODO add your handling code here:

    
        if (Lamda.getText().equals("") || Miu.getText().equals("") || Nilai_s.getText().equals("") )
        {
         JOptionPane.showMessageDialog(null,"Mohon diisi dengan lengkap!!!","WARNING",JOptionPane.INFORMATION_MESSAGE);
        }
        else
        {
        Integer Lamdas = Integer.valueOf(Lamda.getText());
        Double Mius = Double.valueOf(Miu.getText());
        Double Ss = Double.valueOf(Nilai_s.getText());
        
        Operasi Datas = new Operasi(Lamdas,Ss,Mius);
        Operasi2 Datass = new Operasi2(Lamdas,Ss,Mius);
        DecimalFormat df = new DecimalFormat("#.######");
        
        DataI.setText(""+Datas.Data1());
        Hasil_Po.setText(""+df.format(Datass.Po()));
        hasil_pw.setText(""+df.format(Datass.Pw()));
        hasil_Pm.setText(""+df.format(Datass.Pm()));
        hasil_ls.setText(""+df.format(Datass.Ls()));
        hasil_lq.setText(""+df.format(Datass.Lq()));
        hasil_ws.setText(""+df.format(Datass.Ws()));
        hasil_wq.setText(""+df.format(Datass.Wq()));
        }
    }//GEN-LAST:event_OPERASIActionPerformed

